uint16_t sendDataLine(const char *data) {
  memset(txBuffer, 0x00, sizeof(txBuffer));
  strcat(txBuffer, data);
  strcat(txBuffer, "\n");
  uint16_t len = strlen(txBuffer);
  HAL_UART_Transmit(&huart1, (uint8_t *)txBuffer, len, 0xffff);
  while (HAL_UART_GetState(&huart1) == HAL_UART_STATE_BUSY_TX);
  return len;
}

uint16_t sendData(const char *data) {
  uint16_t len = strlen(data);
  HAL_UART_Transmit(&huart1, (uint8_t *)data, len, 0xffff);
  while (HAL_UART_GetState(&huart1) == HAL_UART_STATE_BUSY_TX);
  return len;
}

