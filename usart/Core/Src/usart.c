void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
  if (rxCount >= 255) {
    rxCount = 0;
    memset(rxBuffer, 0x00, BUFFER_SIZE);
    HAL_UART_Transmit(&huart1, (uint8_t *)"data overflow", 14, 0xffff);
  } else {
    rxBuffer[rxCount ++] = charBuffer;
    if (charBuffer == '\n') {
      if (strncmp(rxBuffer, "off", 3) == 0) light_off();
      else if (strncmp(rxBuffer, "on", 2) == 0) light_on();
      else {
        HAL_UART_Transmit(&huart1, (uint8_t *)rxBuffer, rxCount, 0xffff);
        while (HAL_UART_GetState(&huart1) == HAL_UART_STATE_BUSY_TX);
      }
      memset(rxBuffer, 0x00, rxCount);
      rxCount = 0;
    }
    HAL_UART_Receive_IT(&huart1, (uint8_t *)&charBuffer, 1);
  }
}
